T2CON   EQU 0C8H
RCAP2L  EQU 0CAH
RCAP2H  EQU 0CBH

        ORG 0000H
        JMP 0100H

        ORG 0100H
        MOV SCON, #50H      ; Mode 1, ???????????????? (REN=1)
        MOV RCAP2H, #0FFH    
        MOV RCAP2L, #0DCH    
        MOV T2CON, #34H     

Wait_Data:
        JNB RI, $           ; ???????????????? 1
        CLR RI
        MOV A, SBUF         ; ??? ASCII ??????? A
        
        ; ?????????????? '0'-'9' ???????
        CLR C
        SUBB A, #'0'
        JC  Wait_Data       ; ?????????? '0' ??????
        MOV R2, A
        SUBB A, #10
        JNC Wait_Data       ; ?????????? '9' ??????

        ; ????????? 7-Segment
        MOV A, R2
        CALL Display
        JMP Wait_Data

Display:
        MOV DPTR, #Seg_Tab
        MOVC A, @A+DPTR
        MOV P1, A           ; ????????? Port 1 (Active High)
        SETB P3.4           ; ??????????? 1
        SETB P3.5           ; ??????????? 2
        RET

Seg_Tab:
    DB 3FH, 06H, 5BH, 4FH, 66H, 6DH, 7DH, 07H, 7FH, 6FH
    END